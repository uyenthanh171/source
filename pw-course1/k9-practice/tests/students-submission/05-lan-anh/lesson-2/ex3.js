//a. Khai báo một hằng số với giá trị kiểu Number bất kỳ
const Number = 20;
//i. Nếu hằng số lớn hơn 0 in: "Giá trị bạn nhập là số dương"
if (Number > 0){
    console.log("Giá trị bạn nhập là số dương");
}
//ii. Nếu hằng số nhỏ hơn 0 in: "Giá trị bạn nhập là số âm"
if (Number < 0){
    console.log("Giá trị bạn nhập là số âm");
}
//iii. Nếu hằng số = 0 in: "Giá trị bạn nhập là số 0"
if (Number == 0){
    console.log("Giá trị bạn nhập là số 0");
}