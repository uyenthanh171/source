//Phương thức tách chuỗi: string.split(separator, limit)
let str = "Hello";
let strsplit = str.split("");//Tách chuỗi theo chữ cái thì để rỗng
console.log(strsplit);
//Phương thức đảo ngược mảng kí tự: reverse()
let splrvs = strsplit.reverse();
console.log(splrvs);
//Phương thức nối các kí tự lại thành chuỗi: join()
let splrvsjn = splrvs.join();
console.log(splrvsjn);